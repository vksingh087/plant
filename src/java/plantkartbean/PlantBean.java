
package plantkartbean;


public class PlantBean {
    private int pid;
    private String plantName;
    private String plantDetail;
    private int price;
    private int qunatity;
    private int sellQuantity;
    private String paymentStatus;

    /**
     * @return the pid
     */
    public int getPid() {
        return pid;
    }

    /**
     * @param pid the pid to set
     */
    public void setPid(int pid) {
        this.pid = pid;
    }

    /**
     * @return the plantName
     */
    public String getPlantName() {
        return plantName;
    }

    /**
     * @param plantName the plantName to set
     */
    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }

    /**
     * @return the plantDetail
     */
    public String getPlantDetail() {
        return plantDetail;
    }

    /**
     * @param plantDetail the plantDetail to set
     */
    public void setPlantDetail(String plantDetail) {
        this.plantDetail = plantDetail;
    }

    /**
     * @return the price
     */
    public int getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(int price) {
        this.price = price;
    }

    /**
     * @return the qunatity
     */
    public int getQunatity() {
        return qunatity;
    }

    /**
     * @param qunatity the qunatity to set
     */
    public void setQunatity(int qunatity) {
        this.qunatity = qunatity;
    }

    /**
     * @return the sellQuantity
     */
    public int getSellQuantity() {
        return sellQuantity;
    }

    /**
     * @param sellQuantity the sellQuantity to set
     */
    public void setSellQuantity(int sellQuantity) {
        this.sellQuantity = sellQuantity;
    }

    /**
     * @return the paymentStatus
     */
    public String getPaymentStatus() {
        return paymentStatus;
    }

    /**
     * @param paymentStatus the paymentStatus to set
     */
    public void setPaymentStatus(String paymentStatus) {
        this.paymentStatus = paymentStatus;
    }
    
    
}
